

public class SwitchStatements {
	public static void main(String[] args) {
	
	int age;
	age = 3;
	
	
	switch (age) {
	case 1:
		System.out.println("You are sweet");
		break;
	case 2:
		System.out.println("You are in your terrible two's");
		break;
	case 3:
		System.out.println("You can start going to Day Care");
		break;
	default:
		System.out.println("You are going to school");
		break;
				
		
	}

	}

}
